##WAP to accept a number from 1 to 7 and display the name of day
##like 1 for sunday, 2 for monday, etc.

number=int(input("Enter the day number(1-7): "))
if number==1:
        print("Sunday")
elif number==2:
        print("Monday")
elif number==3:
        print("Tuesday")
elif number==4:
        print("Wednesday")
elif number==5:
        print("Thursday")
elif number==6:
        print("Friday")
elif number==7:
        print("Saturday")
